#include "bits/stdc++.h"
using namespace std;

const int N = 1e5 + 5;
int parent[N];
int n, m;

int main() {

        scanf("%d", &n);

        for(int i = 1; i <= n; i++) {
                scanf("%d", &parent[i]);
        }

        scanf("%d", &m);

        while(m--) {
                int node, pos; scanf("%d %d", &node, &pos);
        }


        return 0;
}
